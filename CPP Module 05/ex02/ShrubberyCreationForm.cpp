// Canonical form implementation

// Default constructor
ShrubberyCreationForm::ShrubberyCreationForm()
	: AForm("ShrubberyCreationForm", 145, 137), target("Default Target") {
}

// Constructor with parameters
ShrubberyCreationForm::ShrubberyCreationForm(const std::string &target)
	: AForm("ShrubberyCreationForm", 145, 137), target(target) {
}

// Copy constructor
ShrubberyCreationForm::ShrubberyCreationForm(const ShrubberyCreationForm &copy)
	: AForm(copy), target(copy.target) {
}

// Assignment operator
ShrubberyCreationForm &ShrubberyCreationForm::operator=(const ShrubberyCreationForm &copy) {
	if (this != &copy) {
		AForm::operator=(copy);
	}
	return *this;
}

// Destructor
ShrubberyCreationForm::~ShrubberyCreationForm() {
}

// Member functions implementation

// Execute the form
void ShrubberyCreationForm::execute(const Bureaucrat &executor) const {
	if (!getIsSigned()) {
		throw FormNotSignedException();
	}
	else if (executor.getGrade() > getExecuteGrade()) {
		throw GradeTooLowException();
	}
	std::string filename = target + "_shrubbery";
	std::ofstream file(filename.c_str());
	if (!file.is_open()) {
		throw FileOpenException();
	}
	write_ASCII_tree(file);
	std::cout << "Shrubbery created in file: " << filename << std::endl;
	file.close();
}

